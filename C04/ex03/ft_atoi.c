int		ft_atoi(char *str)
{
	int i;
	int less;
	int nb;

	nb = 0;
	i = 0;
	less = 0;
	while (str[i] == '\t' || str[i] == '\v' || str[i] == '\n'
			|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		less += (str[i] == '-') ? 1 : 0;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return ((less % 2 == 0) ? nb : -nb);
}
