int		ft_str_is_printable(char *str)
{
	int i;

	if (!str)
		return (1);
	i = -1;
	while (str[++i])
	{
		if ((str[i] < 32 && str[i] >= 0) || str[i] == 127)
			return (0);
	}
	return (1);
}
