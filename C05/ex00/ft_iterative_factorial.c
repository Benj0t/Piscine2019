int		ft_iterative_factorial(int nb)
{
	int n;

	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	n = nb - 1;
	while (n > 0)
	{
		nb = nb * n--;
	}
	return (nb);
}
