#include <unistd.h>

void ft_putchar(char c)
{
	write(1, &c, 1);
}

int main()
{
	char a;
	int i;

	a = 'a' - 1;
	i = 0;
	while (++a <= 'z')
		ft_putchar((i++ % 2 == 0) ? a : a - 32);
	ft_putchar('\n');
	return(0);
}
