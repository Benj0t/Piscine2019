int ft_atoi(const char *str)
{
	int neg;
	int i;
	int nb;

	neg = 0;
	i = 0;
	nb = 0;
	while(str[i] == '\t'|| str[i] == '\v'|| str[i] == '\n'|| str[i] == '\f'|| str[i] == '\r'|| str[i] == ' ')
		i++;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
		nb = nb*10 + (str[i++] - '0');
	return((neg == 1) ? -nb : nb);
}
